/*
 *  数据写入
 */
void OLED_WrDat(uint8_t data)
{
    uint8_t i = 8;

    OLED_DC_OH;
    //asm ("nop");
    OLED_D0_OL;
    //asm ("nop"); 
    while (i --)
    {
        if (data & 0x80)
        {
            OLED_D1_OH;
        }
        else
        {
            OLED_D1_OL;
        }
        OLED_D0_OH; 
        //asm ("nop");
        //asm ("nop");
        OLED_D0_OL;    
        data <<= 1;    
    }
}

/*
 *  命令写入
 */
void OLED_WrCmd(uint8_t cmd)
{
    uint8_t i = 8;

    OLED_DC_OL;
    OLED_D0_OL;
    //asm ("nop");  
    while (i --)
    {
        if (cmd & 0x80)
        {
            OLED_D1_OH;
        }
        else
        {
            OLED_D1_OL;
        }
        OLED_D0_OH;
        //asm ("nop");
        //asm("nop");             
        OLED_D0_OL;    
        cmd <<= 1;  
    } 	
}

/*
 *  坐标设置
 */
void OLED_Set_XY(uint8_t x, uint8_t y)
{ 
    OLED_WrCmd(0xb0 + y);
    OLED_WrCmd(((x & 0xf0) >> 4) | 0x10);
    OLED_WrCmd((x & 0x0f) | 0x01); 
}

/*
 *  清屏函数
 */
void OLED_Clear(void)
{
    uint8_t y,x;	
    
    for (y=0; y<8; y++)
    {
        OLED_WrCmd(0xb0 + y);
        OLED_WrCmd(0x01);
        OLED_WrCmd(0x10); 
        for (x=0; x<X_WIDTH; x++)
        {
            OLED_WrDat(0);
        }
    }
}
/*
 *  清除x1到x2，y1到y2
 */
void OLED_Clear_XY(uint8_t x1,uint8_t x2,uint8_t y1,uint8_t y2)
{
    uint8_t y,x;	
    
    for (y=y1; y<y2; y++)
    {
        OLED_WrCmd(0xb0 + y);
        OLED_WrCmd(0x01);
        OLED_WrCmd(0x10); 
        for (x=x1; x<x2; x++)
        {
            OLED_WrDat(0);
        }
    }
}
/*
 *  延时函数
 */
void OLED_Delay_ms(uint16_t ms)
{                         
    uint16_t a;

    while (ms)
    {
        a = 13350;
        while (a--);
        ms --;
    }
    return;
}




void OLED_Pin_Init(GPIO_Type *GPIOx,uint8_t GPIO_Pin)
{
	GPIO_InitTypeDef GPIO_InitStruct1;

	GPIO_InitStruct1.GPIOx = GPIOx;                           
	GPIO_InitStruct1.GPIO_InitState = Bit_SET;               
	GPIO_InitStruct1.GPIO_IRQMode = GPIO_IT_DISABLE;          //不时能中断
	GPIO_InitStruct1.GPIO_Pin = GPIO_Pin;                  
	GPIO_InitStruct1.GPIO_Mode = GPIO_Mode_OPP;

	//执行GPIO初始化
	GPIO_Init(&GPIO_InitStruct1);	
}



/*
 *  液晶初始化
 */
void OLED_Init(void)        
{
    OLED_Pin_Init(OLED_PORT, OLED_SCL);     //GPIO_Init(OLED_PORT, OLED_SCL, OUT, 1);
    OLED_Pin_Init(OLED_PORT, OLED_SDA);     //GPIO_Init(OLED_PORT, OLED_SDA, OUT, 1);
    OLED_Pin_Init(OLED_PORT, OLED_RST);     //GPIO_Init(OLED_PORT, OLED_RST, OUT, 1);
    OLED_Pin_Init(OLED_PORT, OLED_DC);     //GPIO_Init(OLED_PORT, OLED_DC, OUT, 1);     //配置OLED端口引脚为GPIO输出

    OLED_D0_OH; 	

    OLED_RST_OL;
    OLED_Delay_ms(50);
    OLED_RST_OH;

    /* 从上电到下面开始初始化要有足够的时间，即等待RC复位完毕 */
    OLED_WrCmd(0xAE | 0x00);	 // Display Off (0x00)

    OLED_WrCmd(0xD5);		
    OLED_WrCmd(0x80);		     // Set Clock as 100 Frames/Sec

    OLED_WrCmd(0xA8);	
    OLED_WrCmd(0x3F);	         // 1/64 Duty (0x0F~0x3F)

    OLED_WrCmd(0xD3);	
    OLED_WrCmd(0x00);		     // Shift Mapping RAM Counter (0x00~0x3F)

    OLED_WrCmd(0x40 | 0x00);     // Set Mapping RAM Display Start Line (0x00~0x3F)

    OLED_WrCmd(0x8D);
    OLED_WrCmd(0x10 | 0x04);	 // Enable Embedded DC/DC Converter (0x00/0x04)

    OLED_WrCmd(0x20);
    OLED_WrCmd(0x02);		     // Set Page Addressing Mode (0x00/0x01/0x02)

    OLED_WrCmd(0xA0 | 0x01);     // Set SEG/Column Mapping     0x00左右反置 0x01正常

    OLED_WrCmd(0xC0 | 0x08);     // Set COM/x Scan Direction   0x00上下反置 0x08正常

    OLED_WrCmd(0xDA);	
    OLED_WrCmd(0x02 | 0x10);     // Set Sequential Configuration (0x00/0x10)

    OLED_WrCmd(0x81);
    OLED_WrCmd(0xCF);	         // Set SEG Output Current

    OLED_WrCmd(0xD9);		 	
    OLED_WrCmd(0xF1);            // Set Pre-Charge as 15 Clocks & Discharge as 1 Clock

    OLED_WrCmd(0xDB);
    OLED_WrCmd(0x40);	         // Set VCOM Deselect Level

    OLED_WrCmd(0xA4 | 0x00);	 // Disable Entire Display On (0x00/0x01)

    OLED_WrCmd(0xA6 | 0x00);	 // Disable Inverse Display On (0x00/0x01)

    OLED_WrCmd(0xAE | 0x01);     // Display On (0x01)

    OLED_Clear();                //初始清屏
    OLED_Set_XY(0,0); 
}
