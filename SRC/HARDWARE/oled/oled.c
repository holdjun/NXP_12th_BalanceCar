void OLED_Write_Num4(uint8_t x, uint8_t y, int16_t num)
{
	if(num<0)
	{
		num=-num;
	}
    OLED_Write_Char(x,     y, num / 1000);
    OLED_Write_Char(x + 1, y, num % 1000 / 100);
    OLED_Write_Char(x + 2, y, num % 100 / 10);
    OLED_Write_Char(x + 3, y, num % 10);

} 


